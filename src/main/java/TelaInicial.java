    private void btnPercentualVotosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPercentualVotosActionPerformed
        // TODO add your handling code here:
        int votos_validos, votos_brancos, votos_nulos, total_votos;
        
        votos_validos = Integer.parseInt(txtVotosValidos.getText());
        votos_brancos = Integer.parseInt(txtVotosBrancos.getText());
        votos_nulos = Integer.parseInt(txtVotosNulos.getText());
        
        total_votos = votos_validos + votos_brancos + votos_nulos;
        
        lblTotalVotos.setText(Integer.toString(total_votos));
        
        ContagemVotos contagem1 = new ContagemVotos(total_votos, votos_validos, votos_brancos, votos_nulos);
        
        txtareaPercentual.setText("Percentual de votos validos: " + Integer.toString(contagem1.totalVotosValidos()) + "%");
        txtareaPercentual.setText(txtareaPercentual.getText() + "\n" + "Percentual de votos brancos: " + Integer.toString(contagem1.totalVotosBrancos()) + "%");
        txtareaPercentual.setText(txtareaPercentual.getText() + "\n" + "Percentual de votos nulos: " + Integer.toString(contagem1.totalVotosNulos()) + "%");
    }//GEN-LAST:event_btnPercentualVotosActionPerformed

