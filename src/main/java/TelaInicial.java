    private void btnBubbleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBubbleActionPerformed
        // TODO add your handling code here:
        
        int vetor[] = new int[10];
        txtareaBubble.setText("Vetor original: " + "\n"  + "[");
        for (int i =0; i< 10; i ++){
            vetor[i] = (int)Math.round(Math.random() * 100);
            txtareaBubble.setText(txtareaBubble.getText()+ "," + vetor[i]);
        }
        txtareaBubble.setText(txtareaBubble.getText()+ "]" + "\n");
        
        BubbleSort sort1 = new BubbleSort(vetor);
        sort1.ordenar();
        vetor = sort1.getVetor();
        
        txtareaBubble.setText(txtareaBubble.getText()+ "vetor ordenado" + "\n" + "[");
        for (int i =0; i< 10; i ++){
            txtareaBubble.setText(txtareaBubble.getText()+ "," + vetor[i]);
        }
        txtareaBubble.setText(txtareaBubble.getText()+ "]" + "\n");
    }//GEN-LAST:event_btnBubbleActionPerformed

